    private void show_profile()
    {
        if(pr.getType().equals("Driver"))
        {
            Drivers dr = (Drivers) pr;
            jTextField1.setText(dr.getName());
            jTextField2.setText(dr.getFathername());
            jTextField3.setText(dr.getCnic());
            jTextField4.setText(dr.getDOB());
            jTextField5.setText(dr.getNumber());
            jTextField6.setText(dr.getGender());
            jTextField7.setText(dr.getEmail());
            jTextField8.setText(dr.getType());
            jTextField9.setText(dr.getRecruit_test_marks()+"");
            File f = new File("..\\Ambulance Managemenr System\\src\\Images\\"+dr.getDriving_licence());
            try {
                BufferedImage img = null;
            img = ImageIO.read(new File(f.getAbsolutePath()));
            Image img1= img.getScaledInstance(AttachFileLabel.getWidth(),AttachFileLabel.getHeight(),Image.SCALE_SMOOTH);
            ImageIcon format = new ImageIcon(img1);
            AttachFileLabel.setIcon(format);
            } catch (Exception e) {
            }
        }
        else
        {
            EMT dr = (EMT) pr;
            jTextField1.setText(dr.getName());
            jTextField2.setText(dr.getFathername());
            jTextField3.setText(dr.getCnic());
            jTextField4.setText(dr.getDOB());
            jTextField5.setText(dr.getNumber());
            jTextField6.setText(dr.getGender());
            jTextField7.setText(dr.getEmail());
            jTextField8.setText(dr.getType());
            jTextField9.setText(dr.getRecruit_test_marks()+"");
            File f = new File("..\\Ambulance Managemenr System\\src\\Images\\"+dr.getCertificate());
            try {
                BufferedImage img = null;
            img = ImageIO.read(new File(f.getAbsolutePath()));
            Image img1= img.getScaledInstance(AttachFileLabel.getWidth(),AttachFileLabel.getHeight(),Image.SCALE_SMOOTH);
            ImageIcon format = new ImageIcon(img1);
            AttachFileLabel.setIcon(format);
            } catch (Exception e) {
            }
        }    
        
    }

