    private void LoginButtom1MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_LoginButtom1MouseClicked
        // TODO add your handling code here:
       if(this.pr.getType().equals("Driver"))
       {
           Drivers dr = (Drivers) pr;
           String a = jComboBox1.getSelectedItem().toString();
           Person per = new Drivers( a, dr.getName(), dr.getFathername(), dr.getCnic(), dr.getDOB(), dr.getGender(),dr.getEmail(), dr.getNumber(), dr.getDriving_licence(), 0);
           Drivers tr = (Drivers)per;
           tr.setOff_id(off_id);
           ams.getPerson_list().insert(per);
           this.setVisible(false);
       }
       else
       {
       EMT dr = (EMT) pr;
           String a = jComboBox1.getSelectedItem().toString();
           Person per = new EMT( a, dr.getName(), dr.getFathername(), dr.getCnic(), dr.getDOB(), dr.getGender(),dr.getEmail(), dr.getNumber(), dr.getCertificate(), 0);
           EMT tr = (EMT)per;
           tr.setOff_id(off_id);
           ams.getPerson_list().insert(per);
           this.setVisible(false);
       
       }

    }//GEN-LAST:event_LoginButtom1MouseClicked

